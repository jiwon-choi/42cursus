#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t		l;
	char		*dup;
	int			i;

	l = ft_strlen(s);
	if (!(dup = (char *)malloc(l + 1)))
		return (0);
	i = 0;
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = 0;
	return (dup);
}
